public class UserRepository {
	
	public void save(UserEntity entity)
	{
		
		Configuration configuration=new Configuration();
		configuration.setProperties(ConnectionPropertiesUtil.getMySqlDbProperties());
		configuration.addAnnotatedClass(UserEntity.class);
		SessionFactory sessionFactory = configuration.buildSessionFactory();
		Session session = sessionFactory.openSession();
		Transaction transaction = session.beginTransaction();
		session.save(entity);
		transaction.commit();

		
	
	}
	public UserEntity findById(Long id)
	{
		
		Configuration cfg =new Configuration();
		cfg.setProperties(ConnectionPropertiesUtil.getMySqlDbProperties());
		cfg.addAnnotatedClass(UserEntity.class);
		SessionFactory factory= cfg.buildSessionFactory();
        Session session=factory.openSession();
        return session.get(UserEntity.class, id);
     
      
        }
	
	public void update(UserDto dto)
	{
		
		UserEntity findById = findById(dto.getAltKey());
if(findById!=null) {
	 Configuration cfg=new Configuration();
	 cfg.setProperties(ConnectionPropertiesUtil.getMySqlDbProperties());
	cfg.addAnnotatedClass(UserEntity.class);
	SessionFactory factory=cfg.buildSessionFactory();
	Session session=factory.openSession();
	Transaction transaction=session.beginTransaction();
	findById.setCountry(dto.getCountry());
	findById.setCity(dto.getCity());
	findById.setPinCode(dto.getPinCode());
	findById.setEmail(dto.getEmail());
	findById.setName(dto.getName());
	session.merge(findById);
	transaction.commit();
}
	
	 
	 
	}
	public void delete(long id)
	{
		UserEntity findById = findById(id);
		SessionFactory factory=SessionFactoryUtil.getSessionFactory();
		Session session=factory.openSession();
		Transaction transaction=session.beginTransaction();
		session.delete(findById);
		transaction.commit();
	}
	
	public List<UserEntity> findall()
	{
		StringBuilder builder=new StringBuilder();
		builder.append("from UserEntity");
		SessionFactory factory=SessionFactoryUtil.getSessionFactory();
		Session session=factory.openSession();
		Query createQuery = session.createQuery(builder.toString());
		 return createQuery.getResultList();
		
		
	}
	public List<UserEntity> findByName(String name)
	{
		StringBuilder builder=new StringBuilder();
		builder.append("from UserEntity where name=:n");
		SessionFactory factory=SessionFactoryUtil.getSessionFactory();
		Session session=factory.openSession();
	    Query createQuery = session.createQuery(builder.toString());
	    createQuery.setParameter("n", name);
	    return createQuery.getResultList();
	    
	}
	public void UpdateByemail(String password,String email)
	{
		StringBuilder builder=new StringBuilder();
		builder.append(" update UserEntity set password=:p where email=:e ");
		SessionFactory factory=SessionFactoryUtil.getSessionFactory();
		Session session=factory.openSession();
		Transaction transaction=session.beginTransaction();
		Query createQuery = session.createQuery(builder.toString());
		
		createQuery.setParameter("p", password);
		createQuery.setParameter("e", email);
		 createQuery.executeUpdate();
		 transaction.commit();
		 
	
	}
	public void DeleteUserByEmail(String email)
	{
		StringBuilder builder=new StringBuilder();
		builder.append(" delete UserEntity where email=:e");
		SessionFactory factory=SessionFactoryUtil.getSessionFactory();
		Session session=factory.openSession();
		Transaction transaction=session.beginTransaction();
		Query createQuery = session.createQuery(builder.toString());
		createQuery.setParameter("e", email);
		createQuery.executeUpdate();
		transaction.commit();
		
	}
	public void saveTeamDetail(Team team)
	{
		SessionFactory factory= SessionFactoryUtil.getSessionFactory();
		Session session=factory.openSession();
		Transaction transaction=session.beginTransaction();
		session.merge(team);
		transaction.commit();
	}
	public void saveFlightDetails(Filght filght)
	{
		SessionFactory factory=SessionFactoryUtil.getSessionFactory();
		Session session=factory.openSession();
		Transaction transaction=session.beginTransaction();
		session.save(filght);
		transaction.commit();
	}
	public void saveProductDetails(Product product)
	{
		SessionFactory factory=SessionFactoryUtil.getSessionFactory();
		Session session=factory.openSession();
		Transaction  transaction=session.beginTransaction();
		session.save(product);
		transaction.commit();
	}

}
